<b>Guide de l'utilisateur</b><br> <br>
<br>1. Répondez à chaque question par "Pour", "Neutre", "Contre" ou "Je passe". Une fois répondu, vous allez passer automatiquement à la question suivante. 
<br><br>2. En répondant à la dernière question vous allez passer à l'interface suivante pour choisir les questions qui vous sont les plus importantes.
<br><br>3. Cochez les questions qui vous sont les plus importantes. Chaque question cochée compte double lors du calcul du résultat. Vous avez la possibilité de corriger vos réponses avant de passer à l'étape suivante.
<br><br>4. En cliquant sur "Résultat" la liste des partis s'affichera par ordre d'affinités à vos réponses. Les partis les plus proches de vos convictions seront affichés en haut du tableau. 
<br><br>5. Cochez les partis avec lesquels vous désirez comparer vos réponses et cliquez sur "Afficher les partis cochés" pour voir le tableau des réponses.
<br><br>6. Vous avez la possibilité de changer les partis à comparer ou l'importance des réponses en cliquant sur "Changer la sélection des partis" ou "Changer l'importance des réponses."   
<br><br><b>Utilités</b>
<br>- Les questions que vous passez en cliquant "Je passe" ne seront pas prises en considération dans votre résutat. </li>
<br>- En cliquant sur le nom d'un parti ou sur son logo, vous aurez des liens pour la fiche du parti, son site internet et facebook (s'ils existent). 
<br>- En cliquant sur la réponse d'un parti, vous verrez sa justification par rapport à sa réponse (s'il en a saisi une). 

