<b>Qui sommes nous?</b><br> <br>
L'équipe de TuniVote est principalement composée de membres de l’association <a href="http://www.tunicomp.net" target="_blank"><font color=#CD0000 size=2>tunicomp</font></a> (réseau des compétences tunisiennes en Allemagne), 
qui est une association à but non lucratif, indépendante et politiquement neutre, fondée par des tunisiens résidant en Allemagne ayant comme objectif: Rassembler les forces et compétences des cadres tunisiens germanophones, les sensibiliser et canaliser leurs efforts au service de l'avènement d'une Tunisie libre, prospère et tolérante.
<br><br>tunicomp concentre toutes les ressources mobilisées et l'expertise de ses membres pour développer et mettre en œuvre des actions concrètes dans les domaines de l'aide humanitaire, le développement économique, la culture sociopolitique et le travail médiatique pour le bien de la Tunisie. 
<br><br>Pour le projet TuniVote, nous avons tenu à choisir des partenaires indépendants sur place en Tunisie qui s’identifient avec notre initiative et s'engagent avec nous pour la réalisation de ce projet assez complexe, nécessitant des recherches, une gestion et une logistique fondées. Le choix a été fait
sur l'association <a href="http://www.nou-r.blogspot.com/" target="_blank"><font color=#CD0000 size=2>NOU-R</font></a> qui nous a été un renfort très important du point de vue logistique, ressources, contacts et gestion. Nos experts sont entre autre Mr. Abdelkader Jedidi, professeur universitaire et producteur de l'émission télévisée "Le dictionnaire politique" au cours de laquelle il présentait 
quotidiennement un parti sur le plateau de la télévision nationale 1. De plus nous avons coopéré avec <a href="http://pactetunisien.org" target="_blank"><font color=#CD0000 size=2>PaCTE</font></a> et <a href="http://www.partistunisie.com" target="_blank"><font color=#CD0000 size=2>www.partistunisie.com</font></a> pour enrichir le projet et optimiser la qualité de TuniVote pour l'électeur tunisien. 
