<b>À propos de TuniVote</b><br> <br>
Le 23 octobre, le Tunisien va voter pour la première fois librement, mais avec plus que 100 partis politiques c’est difficile de les connaître tous.  
Beaucoup d’informations et surtout beaucoup d’intox et de préjugés engendrent une grande confusion de la part du citoyen qui ne sait pas quelle est la différence entre les partis? Pour qui voter? Pourquoi voter?

<br><br>   Consciente de ce problème, l’association <a href="http://www.tunicomp.net" target="_blank"><font color=#CD0000 size=2>tunicomp</font></a> (réseau des compétences tunisiennes en Allemagne), inspirée par le modèle allemand, a eu l’initiative avec ses partenaires de développer un outil en ligne d’aide au vote, baptisé « TuniVote ».   
Cet outil aide les électeurs tunisiens à déterminer leurs préférences politiques en vue des élections de l'assemblée constituante du 23 octobre 2011. « TuniVote » contient une liste de questions couvrant tous les domaines qui touchent notre société: l'économie, l'éducation, la religion, la culture, la santé, la famille, la sûreté et bien sûr le système politique. Après avoir répondu aux questions « TuniVote » vous indique le parti le plus proche de vos opinions, en se basant sur une comparaison avec les réponses des partis politiques sur les mêmes questions. Les partis apparaîtront par ordre d'affinité avec vos réponses. Vous pourrez aussi comparer les réponses données par les partis avec vos propres réponses. 

<br><br>L'intégralité du concept « TuniVote », la rédaction des questions et l’application internet ont été réalisées uniquement par des compétences tunisiennes, sans l'aide d'une tierce partie étrangère.

<br><br>« TuniVote » n’est qu’un guide et ne prétend pas vous donner la réponse définitive à la question: « Pour qui allez-vous voter ? » Nous vous encourageons à lire les engagements politiques des partis avec attention afin de faire un choix délibéré.
